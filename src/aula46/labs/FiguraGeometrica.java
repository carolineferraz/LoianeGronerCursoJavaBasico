package aula46.labs;

public abstract class FiguraGeometrica {
	
	private String nome;
	private String cor;
	
	public String getNome() {
		return nome;
	}
	public String getCor() {
		return cor;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	
	

}
